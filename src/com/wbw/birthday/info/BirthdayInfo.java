package com.wbw.birthday.info;

public class BirthdayInfo {
	private String name;
	private String remarkString;
	private int kind;
	private int year;
	private int month;
	private int day;
	private String timeofday;
	private int alarmkind;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRemarkString() {
		return remarkString;
	}
	public void setRemarkString(String remarkString) {
		this.remarkString = remarkString;
	}
	public int getKind() {
		return kind;
	}
	public void setKind(int kind) {
		this.kind = kind;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getTimeofday() {
		return timeofday;
	}
	public void setTimeofday(String timeofday) {
		this.timeofday = timeofday;
	}
	public int getAlarmkind() {
		return alarmkind;
	}
	public void setAlarmkind(int alarmkind) {
		this.alarmkind = alarmkind;
	}
	
}
